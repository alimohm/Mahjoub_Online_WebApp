# run.py
# الملف الرئيسي لتشغيل محرك منصة محجوب أونلاين السيادي
from core import create_app, db

# بناء التطبيق باستخدام المصنع (Factory Pattern)
app = create_app()

if __name__ == "__main__":
    with app.app_context():
        # التأكد من إنشاء جميع الجداول (الترسانة الرقمية) قبل التشغيل
        # هذه الخطوة تضمن استقرار قاعدة البيانات عند أول إقلاع
        try:
            db.create_all()
            print("✅ تم فحص وإنشاء جداول قاعدة البيانات بنجاح.")
        except Exception as e:
            print(f"⚠️ فشل في تهيئة قاعدة البيانات: {e}")

    # تشغيل السيرفر؛ تم ضبط المنفذ 8080 ليتوافق مع إعدادات Railway الافتراضية
    app.run(host='0.0.0.0', port=8080)
